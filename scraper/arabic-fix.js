// أخطاء إملائية/استخراجية متكررة في هذه الملفات تحديداً
const MISC_FIXES = [
  [/ق ارر/g, 'قرار'],       // انكسار "قرار" بمسافة داخلية
  [/الق ارر/g, 'القرار'],
  [/بغ ارمة/g, 'بغرامة'],
  [/غ ارمة/g, 'غرامة'],
  [/اج ارءات/g, 'اجراءات'],
  [/إج ارءات/g, 'إجراءات'],
  [/\)\s*(\d+)\s*\(/g, '($1)'],          // )1( -> (1)
  [/\)\s*([\u0660-\u0669]+)\s*\(/g, '($1)'], // أرقام هندية
  [/\)([^()\n]{1,60}?)\(/g, '($1)'],       // )ستة أشهر( -> (ستة أشهر)
  [/جمهور ية/g, 'جمهورية'],
  [/قانو ن/g, 'قانون'],
];

/** يزيل الكشيدة ويطبّع أشكال العرض (Presentation Forms) إلى الحروف القياسية */
function normalizePresentation(text) {
  return text.normalize('NFKC').replace(TATWEEL, '');
}

/** الإصلاح الكامل لنص مستخرج من PDF */
function fixArabicPdfText(text) {
  let t = normalizePresentation(text);

  for (const [re, rep] of MISC_FIXES) t = t.replace(re, rep);
  for (const [re, rep] of SAFE_TRIGRAPHS) t = t.replace(re, rep);
  for (const [re, rep] of STANDALONE_FIXES) t = t.replace(re, rep);
  for (const [broken, fixed] of WORD_FIXES) {
    if (broken === '__skip__') continue;
    t = t.split(broken).join(fixed);
  }

  // قاعدة "يال + حرف" مع حماية الكلمات الصحيحة
  const guards = new Map();
  PROTECTED.forEach((w, i) => {
    const key = `\u0000${i}\u0000`;
    if (t.includes(w)) { guards.set(key, w); t = t.split(w).join(key); }
  });
  t = t.replace(YAL_RULE, 'يلا');
  for (const [key, w] of guards) t = t.split(key).join(w);

  // تنظيف مسافات
  t = t.replace(/[ \t]+/g, ' ').replace(/ ?\n ?/g, '\n').replace(/\n{3,}/g, '\n\n').trim();
  return t;
}

/**
 * يفحص النص بعد الإصلاح ويرجع كلمات "مشبوهة" قد تكون ما تزال مكسورة
 * (نمط: حرف + ا + ل + حرف داخل الكلمة) لغرض المراجعة اليدوية في report.json
 */
function findSuspiciousWords(text) {
  const words = text.split(/[^\u0621-\u064A]+/);
  const sus = new Map();
  const KNOWN_OK = new Set([
    'قال', 'قالت', 'مال', 'حال', 'خالد', 'صالح', 'عالم', 'سالم', 'طالب',
    'المال', 'الحال', 'حالة', 'العدالة', 'رسالة', 'وكالة', 'الوكالة',
    'حالات', 'الحالات', 'مجال', 'المجال', 'أعمال', 'الأعمال', 'أموال',
    'الأموال', 'مقال', 'انتقال', 'الانتقال', 'إرسال', 'الإرسال', 'أشغال',
    'الأشغال', 'مثال', 'أمثال', 'إدخال', 'الثالث', 'الثالثة', 'ثالثاً', 'ثالثا',
    'إجمالي', 'الإجمالي', 'إهمال', 'الإهمال', 'استعمال', 'الاستعمال',
    'إكمال', 'استقبال', 'امتثال', 'الامتثال', 'إبطال', 'الإبطال', 'إبدال',
    'إحلال', 'الإحلال', 'إنزال', 'الإنزال', 'إيصال', 'الإيصال', 'اتصال',
    'الاتصال', 'اتصالات', 'الاتصالات', 'أطفال', 'الأطفال', 'رجال', 'الرجال',
    'عمال', 'العمال', 'جبال', 'الجبال', 'خلال', 'استقلال', 'استغلال',
  ]);
  for (const w of words) {
    if (w.length < 4 || KNOWN_OK.has(w)) continue;
    // تجاهل "ال" التعريف في بداية الكلمة (مع سوابق و/ب/ك/ف/لل) وبعد همزات المقطع
    let body = w.replace(/^[وفبكل]{0,2}ال(?=[\u0621-\u064A])/, '');
    body = body.replace(/^(أ|إ|آ|ا)/, '');
    // نمط داخلي متبقٍ: حرف ثم "ال" ثم حرف — مرشح لأن يكون "لا" معكوسة
    if (/[\u0621-\u064A]ال[\u0621-\u064A]/.test(body)) {
      sus.set(w, (sus.get(w) || 0) + 1);
    }
  }
  return [...sus.entries()].sort((a, b) => b[1] - a[1]).map(([word, count]) => ({ word, count }));
}

module.exports = { fixArabicPdfText, normalizePresentation, findSuspiciousWords };
